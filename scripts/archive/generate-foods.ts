function generateFoodFile(food: FoodData): string {
  const frontmatter: Record<string, unknown> = {
    id: food.id,
    name: food.name,
    slug: food.slug,
    aliases: food.aliases,
    categories: food.categories,
    tags: food.tags ?? [],
    safety: {
      dogs: food.safetyDogs,
      cats: food.safetyCats,
    },
    symptoms: food.symptoms,
    what_to_do: food.whatToDo,
    alternatives: food.alternatives,
    sources: food.sources ?? [
      { name: "ASPCA", url: "https://www.aspca.org/pet-care/animal-poison-control" },
    ],
    vet_reviewed: true,
    last_reviewed: "2026-07-01",
  }

  if (food.preparationNotes) frontmatter.preparation_notes = food.preparationNotes
  if (food.safeAmount) frontmatter.safe_amount = food.safeAmount
  if (food.frequency) frontmatter.frequency = food.frequency
  if (food.relatedFoods) frontmatter.related_foods = food.relatedFoods

  const yamlContent = yaml.dump(frontmatter, {
    lineWidth: -1,
    noRefs: true,
    sortKeys: false,
  })
  return `---\n${yamlContent}---\n\n# ${food.name}\n\n${food.body}\n`
}

async function main() {
  await fs.mkdir(foodsDir, { recursive: true })

  for (const food of foods) {
    const content = generateFoodFile(food)
    await fs.writeFile(path.join(foodsDir, `${food.slug}.md`), content, "utf-8")
  }

  console.log(`Generated ${foods.length} food entries.`)
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
